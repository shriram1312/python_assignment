# Write a Python function to get the largest number, smallest num and sum
# of all from a list.


list=[33,44,5,67,2,30,1,333]  # creating list 

largest=max(list)  # max function() will find maximum number from the list
smallest=min(list) # min() will find smallest number from the list
sum=sum(list)      # sum() will give sumation of all the number from the list
print("\n")

print(f"largest number from the list is:{largest}")
print(f"smallest number from the list is:{smallest}")
print(f"sum of all number from the list is:{sum}")

print("\n")

